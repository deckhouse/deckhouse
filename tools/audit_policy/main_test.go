func TestReplaceAutogeneratedBlock_NormalizesTrailingEmptyLines(t *testing.T) {
	content := strings.Join([]string{
		"prefix",
		autogenBlockStart,
		"old content",
		autogenBlockEnd,
		"",
		"",
		"",
		"suffix",
	}, "\n")

	block := strings.Join([]string{
		autogenBlockStart,
		"new content",
		autogenBlockEnd,
		"",
		"",
	}, "\n")

	got, err := replaceAutogeneratedBlock(content, block)
	if err != nil {
		t.Fatalf("replaceAutogeneratedBlock returned error: %v", err)
	}

	expected := strings.Join([]string{
		"prefix",
		autogenBlockStart,
		"new content",
		autogenBlockEnd,
		"",
		"suffix",
	}, "\n")

	if got != expected {
		t.Fatalf("unexpected replaced content:\n--- got ---\n%s\n--- expected ---\n%s", got, expected)
	}
}

func TestReplaceAutogeneratedBlock_Idempotent(t *testing.T) {
	content := strings.Join([]string{
		"prefix",
		autogenBlockStart,
		"old content",
		autogenBlockEnd,
		"",
		"suffix",
	}, "\n")

	block := strings.Join([]string{
		autogenBlockStart,
		"same content",
		autogenBlockEnd,
	}, "\n")

	once, err := replaceAutogeneratedBlock(content, block)
	if err != nil {
		t.Fatalf("first replaceAutogeneratedBlock returned error: %v", err)
	}
	twice, err := replaceAutogeneratedBlock(once, block)
	if err != nil {
		t.Fatalf("second replaceAutogeneratedBlock returned error: %v", err)
	}

	if once != twice {
		t.Fatalf("replaceAutogeneratedBlock is not idempotent:\n--- once ---\n%s\n--- twice ---\n%s", once, twice)
	}
}

func TestCompactRuleYAML_UsesTwoSpacesIndentation(t *testing.T) {
	rule := audit.PolicyRule{
		Level: audit.LevelMetadata,
		Resources: []audit.GroupResources{
			{
				Group:     "apps",
				Resources: []string{"deployments"},
			},
		},
	}

	yaml, err := compactRuleYAML(rule)
	if err != nil {
		t.Fatalf("compactRuleYAML returned error: %v", err)
	}

	if strings.Contains(yaml, "\t") {
		t.Fatalf("yaml contains tab indentation:\n%s", yaml)
	}
	if !strings.Contains(yaml, "\n  - group: apps\n") {
		t.Fatalf("yaml does not contain expected 2-space list indentation:\n%s", yaml)
	}
}

func TestBuildDetailedAuditRulesPage_HasRuleLinksAndAnchors(t *testing.T) {
	rules, err := cpmhooks.BuiltInAuditPolicyRulesForDocumentation()
	if err != nil {
		t.Fatalf("BuiltInAuditPolicyRulesForDocumentation returned error: %v", err)
	}

	page, err := buildDetailedAuditRulesPage(rules, "en")
	if err != nil {
		t.Fatalf("buildDetailedAuditRulesPage returned error: %v", err)
	}

	if !strings.Contains(page, "- [") || !strings.Contains(page, "](#rule-1)") {
		t.Fatalf("rules list does not contain links to detailed blocks:\n%s", page)
	}
	if !strings.Contains(page, `<a id="rule-1"></a>`) {
		t.Fatalf("detailed rules section does not contain anchor for rule-1")
	}
}
