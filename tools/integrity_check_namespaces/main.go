var (
	configPath string
	rootDir    string

	enabledModulesConfigPath string
	editionName              string
)

type Config struct {
	ExcludeNamespaces    []string `yaml:"excludeNamespaces,omitempty"`
	AdditionalNamespaces []string `yaml:"additionalNamespaces,omitempty"`
}

type namespacesForIntegrity map[string]struct{}
type enabledModules map[string]struct{}

func (s enabledModules) enabled(module string) bool {
	if len(s) == 0 {
		return true
	}

	_, ok := s[module]
	return ok
}

func (o namespacesForIntegrity) String() string {
	list := make([]string, 0, len(o))
	for n := range o {
		list = append(list, n)
	}

	sort.Strings(list)

	outObj := struct {
		NamespacesForIntegrity []string `yaml:"namespaces_for_integrity"`
	}{
		NamespacesForIntegrity: list,
	}

	outBytes, err := yaml.Marshal(outObj)
	if err != nil {
		panic(fmt.Errorf("cannot marshal NamespacesForIntegrity: %v", err))
	}

	return "# Code generated by 'tools/generate_integrity_namespaces.go' DO NOT EDIT.\n" + string(outBytes)
}

func main() {
	initFlags()

	workDir := cwd()
	configDir, err := filepath.Abs(configPath)
	if err != nil {
		panic(err)
	}
	validateConfigDir(configDir)

	isEditionsYaml := enabledModulesConfigPath != "" && strings.HasSuffix(filepath.Clean(enabledModulesConfigPath), "editions.yaml")
	if !isEditionsYaml {
		panic("-edition all requires -enabled-modules-config pointing to editions.yaml")
	}
	editions := parseEditionsFile(enabledModulesConfigPath)

	if editionName == "all" {
		for _, ed := range editions.Editions {
			cfgFile := resolveConfigFile(configDir, ed.Name)
			config := parseConfig(cfgFile)
			render(editions, workDir, config, ed.Name)
		}
	} else {
		cfgFile := resolveConfigFile(configDir, editionName)
		config := parseConfig(cfgFile)
		render(editions, workDir, config, editionName)
	}
}

func logToStdErr(f string, args ...any) {
	_, err := fmt.Fprintf(os.Stderr, f, args...)
	if err != nil {
		panic(err)
	}
}

func writeNamespaces(writer io.Writer, nss namespacesForIntegrity) {
	_, err := writer.Write([]byte(nss.String()))
	if err != nil {
		panic(fmt.Errorf("cannot write NamespacesForIntegrity: %v", err))
	}
}

func initFlags() {
	flag.StringVar(&configPath, "config", "", "Directory with default.yaml and optional <edition>.yaml configs. Required")
	flag.StringVar(&rootDir, "root-dir", "", "Root directory for finding modules. If do not pass calculate from run")
	flag.StringVar(&enabledModulesConfigPath, "enabled-modules-config", "", "Path to yaml config map[string]bool for filter modules")
	flag.StringVar(&editionName, "edition", "", "Edition name (from editions.yaml) or 'all' to generate a file per edition")

	flag.Parse()

	if configPath == "" {
		panic("-config flag is required")
	}
}

func validateConfigDir(dir string) {
	fi, err := os.Stat(dir)
	if err != nil {
		panic(fmt.Errorf("-config must be an existing directory: %w", err))
	}
	if !fi.IsDir() {
		panic(fmt.Errorf("-config must be a directory: %s", dir))
	}
	defaultFile := filepath.Join(dir, "default.yaml")
	if _, err := os.Stat(defaultFile); err != nil {
		panic(fmt.Errorf("config directory must contain default.yaml: %v", err))
	}
}

// resolveConfigFile returns <edition>.yaml in configDir if present, otherwise default.yaml.
func resolveConfigFile(configDir, edition string) string {
	editionFile := filepath.Join(configDir, edition+".yaml")
	if _, err := os.Stat(editionFile); err == nil {
		return editionFile
	}
	return filepath.Join(configDir, "default.yaml")
}

func parseEnabledModulesConfig(editions editionsFile, edition string) enabledModules {
	res := make(enabledModules)
	for _, ed := range editions.Editions {
		if ed.Name == edition {
			for _, module := range ed.AvailableModules {
				res[module] = struct{}{}
			}
		}
	}
	return res
}

// editionsFile is used to read AvailableModules from editions.yaml (key matches the file).
type editionsFile struct {
	Editions []struct {
		Name             string   `yaml:"name,omitempty"`
		AvailableModules []string `yaml:"AvailableModules,omitempty"`
	} `yaml:"editions,omitempty"`
}

func parseEditionsFile(p string) editionsFile {
	content, err := os.ReadFile(p)
	if err != nil {
		panic(fmt.Errorf("cannot read editions file: %v", err))
	}
	var editions editionsFile
	if err := yaml.Unmarshal(content, &editions); err != nil {
		panic(fmt.Errorf("cannot parse editions file: %v", err))
	}
	return editions
}

func parseConfig(p string) Config {
	content, err := os.ReadFile(p)
	if err != nil {
		panic(fmt.Errorf("cannot read config: %v", err))
	}

	result := Config{}

	if err := yaml.Unmarshal(content, &result); err != nil {
		panic(fmt.Errorf("cannot parse config: %v", err))
	}

	return result
}

func cwd() string {
	var err error
	result := ""

	if rootDir != "" {
		result, err = filepath.Abs(rootDir)
		if err != nil {
			panic(err)
		}
	} else {
		_, f, _, ok := runtime.Caller(1)
		if !ok {
			panic("cannot get caller")
		}

		result, err = filepath.Abs(f)
		if err != nil {
			panic(err)
		}

		for i := 0; i < 3; i++ { // ../../
			result = filepath.Dir(result)
		}
	}

	// If deckhouse repo directory is symlinked (e.g. to /deckhouse), resolve the real path.
	// Otherwise, filepath.Walk will ignore all subdirectories.
	result, err = filepath.EvalSymlinks(result)
	if err != nil {
		panic(err)
	}

	return result
}

type moduleMetadata struct {
	Name      string `yaml:"name"`
	Namespace string `yaml:"namespace"`
}

func processModuleDefinition(path string) ( /*name*/ string /*namespace*/, string, error) {
	// if file exists
	var s moduleMetadata
	c, err := os.Open(path)
	if err != nil {
		return "", "", err
	}
	defer c.Close()

	err = yaml.NewDecoder(c).Decode(&s)
	if err != nil {
		return "", "", err
	}

	if s.Namespace == "" {
		// trim file name from path
		dir := filepath.Dir(path)
		ns, err := os.ReadFile(filepath.Join(dir, ".namespace"))
		if err != nil {
			if errors.Is(err, os.ErrNotExist) {
				return "", "", nil
			}
			return "", "", err
		}
		s.Namespace = strings.Trim(string(ns), "\r\n")
	}

	return s.Name, s.Namespace, nil
}

// findModules find all directories, contains module.yaml
// return map of absoulute path to module.yaml -> last directory, where module.yaml is located (xxx-module-name)
func findModules(root string) map[string]string {
	modulesMap := make(map[string]string)

	err := filepath.Walk(root, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}

		if !info.IsDir() {
			return nil
		}

		modulePath := filepath.Join(path, "module.yaml")
		if _, err := os.Stat(modulePath); err != nil {
			return nil
		}

		relPath, err := filepath.Rel(root, path)
		if err != nil {
			return err
		}

		parts := strings.Split(filepath.ToSlash(relPath), "/")
		for _, part := range parts {
			if part == "modules" {
				dirName := filepath.Base(path)
				modulesMap[modulePath] = dirName
				break
			}
		}

		return nil
	})

	if err != nil {
		panic(fmt.Errorf("error walking the path %s: %v", root, err))
	}

	return modulesMap
}

func processModule(moduleYamlPath string, namespacesMap namespacesForIntegrity, modules enabledModules) error {
	name, namespace, err := processModuleDefinition(moduleYamlPath)
	if err != nil {
		return fmt.Errorf("error while processing modules")
	}

	if name == "" {
		return nil
	}

	if !modules.enabled(name) {
		return nil
	}

	if strings.HasPrefix(namespace, "d8-") || namespace == "kube-system" {
		namespacesMap[namespace] = struct{}{}
	}

	return nil
}

func buildIncludesOutputPath(workDir, edition string) string {
	dir := filepath.Join(workDir, "tools", "build_includes")
	if err := os.MkdirAll(dir, 0755); err != nil {
		panic(fmt.Errorf("cannot create build_includes dir: %v", err))
	}
	return filepath.Join(dir, fmt.Sprintf("integrity-check-namespaces-%s.yaml", edition))
}

func collectNamespaces(workDir string, config Config, modulesForFilter enabledModules) namespacesForIntegrity {
	namespacesMap := make(namespacesForIntegrity)
	if len(config.ExcludeNamespaces) > 0 && config.ExcludeNamespaces[0] == "all" {
		return namespacesMap
	}
	res := findModules(workDir)
	for moduleYamlPath := range res {
		err := processModule(moduleYamlPath, namespacesMap, modulesForFilter)
		if err != nil {
			logToStdErr("error process moduleYamlPath: %s", moduleYamlPath)
			panic(err)
		}
	}
	for _, exclude := range config.ExcludeNamespaces {
		delete(namespacesMap, exclude)
	}
	for _, ns := range config.AdditionalNamespaces {
		namespacesMap[ns] = struct{}{}
	}
	return namespacesMap
}

func render(editions editionsFile, workDir string, config Config, edition string) {
	modulesForFilter := parseEnabledModulesConfig(editions, edition)
	namespacesMap := collectNamespaces(workDir, config, modulesForFilter)
	outPath := buildIncludesOutputPath(workDir, edition)
	out, err := os.Create(outPath)
	if err != nil {
		panic(fmt.Errorf("cannot create output: %v", err))
	}
	defer out.Close()
	writeNamespaces(out, namespacesMap)
}
