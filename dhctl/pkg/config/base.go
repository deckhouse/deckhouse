func LoadConfigFromFile(
	ctx context.Context,
	paths []string,
	preparatorProvider MetaConfigPreparatorProvider,
	globalOptions *options.GlobalOptions,
	opts ...ValidateOption,
) (*MetaConfig, error) {
	docs, err := FetchDocuments(paths)
	if err != nil {
		return nil, err
	}

	if globalOptions.EnsureCandiAvailable {
		registryConf, err := buildRegistryConfig(docs)
		if err != nil {
			return nil, err
		}
		if err = prepareCandiDir(ctx, registryConf, globalOptions); err != nil {
			return nil, err
		}
	}

	if err := EnsureProviderBundle(ctx, "", docs, globalOptions); err != nil {
		return nil, err
	}

	// Resolved before ParseConfig: the external preparator binary lives under
	// <DownloadRootDir>/<provider>/.
	downloadRootDir := resolveDownloadRootDir(globalOptions)
	downloadCacheDir := globalOptions.DownloadCacheDir
	if downloadCacheDir == "" {
		downloadCacheDir = filepath.Join(downloadRootDir, "cache")
	}
	opts = append(opts, ValidateOptionDownloadRootDir(downloadRootDir))

	metaConfig, err := ParseConfig(ctx, fs.RevealWildcardPaths(paths), preparatorProvider, globalOptions, opts...)
	if err != nil {
		return nil, err
	}

	metaConfig.DownloadRootDir = downloadRootDir
	metaConfig.DownloadCacheDir = downloadCacheDir
	if globalOptions.DeckhouseDir != "" {
		metaConfig.VersionFilePath = filepath.Join(globalOptions.DeckhouseDir, "version")
	} else {
		metaConfig.VersionFilePath = filepath.Join(options.DefaultDeckhouseDir, "version")
	}

	metaConfig.ShowProgress = globalOptions.ShowProgress

	if metaConfig.ClusterConfig == nil {
		return nil, fmt.Errorf("ClusterConfiguration must be provided")
	}

	versionMapFile := options.DefaultVersionMap
	if globalOptions.VersionMap != "" {
		versionMapFile = globalOptions.VersionMap
	}

	if err := metaConfig.LoadVersionMap(versionMapFile); err != nil {
		return nil, err
	}

	if err := metaConfig.LoadImagesDigests(); err != nil {
		return nil, err
	}

	if err := metaConfig.LoadInstallerVersion(); err != nil {
		return nil, err
	}

	if metaConfig.ClusterType == CloudClusterType && len(metaConfig.ProviderClusterConfig) == 0 {
		if ProviderRequiresClusterConfig(metaConfig.ProviderName) {
			return nil, fmt.Errorf("ProviderClusterConfiguration section is required for a Cloud cluster.")
		}
	}

	return metaConfig, nil
}

func numerateManifestLines(manifest []byte) string {
	manifestLines := strings.Split(string(manifest), "\n")
	builder := strings.Builder{}

	for index, line := range manifestLines {
		fmt.Fprintf(&builder, "%d\t%s\n", index+1, line)
	}

	return builder.String()
}

func ParseConfig(
	ctx context.Context,
	paths []string,
	preparatorProvider MetaConfigPreparatorProvider,
	globalOptions *options.GlobalOptions,
	opts ...ValidateOption,
) (*MetaConfig, error) {
	content := ""
	for _, path := range paths {
		if strings.Contains(path, "*") {
			continue // skip wildcard paths, we revealed them in the previous step
		}

		log.DebugF("Have config file %s\n", path)
		fileContent, err := os.ReadFile(path)
		if err != nil {
			return nil, fmt.Errorf("loading config file: %v", err)
		}
		content = content + "\n\n---\n\n" + string(fileContent)
	}

	return ParseConfigFromData(ctx, content, preparatorProvider, globalOptions, opts...)
}

func ParseConfigFromCluster(
	ctx context.Context,
	kubeCl *client.KubernetesClient,
	preparatorProvider MetaConfigPreparatorProvider,
	globalOptions *options.GlobalOptions,
	operation string,
) (*MetaConfig, error) {
	var metaConfig *MetaConfig
	var err error

	return metaConfig, log.ProcessCtx(ctx, "common", "Get cluster configuration", func(ctx context.Context) error {
		return retry.NewLoop("Get cluster configuration from Kubernetes cluster", 50, 1*time.Second).
			RunContext(ctx, func() error {
				metaConfig, err = parseConfigFromCluster(ctx, kubeCl, preparatorProvider, globalOptions, operation)
				return err
			})
	})
}

func ParseConfigInCluster(
	ctx context.Context,
	kubeCl *client.KubernetesClient,
	preparatorProvider MetaConfigPreparatorProvider,
	globalOptions *options.GlobalOptions,
	operation string,
) (*MetaConfig, error) {
	var (
		metaConfig *MetaConfig
		err        error
	)

	err = retry.NewSilentLoop("Get cluster configuration from inside Kubernetes cluster", 25, 1*time.Second).
		RunContext(ctx, func() error {
			metaConfig, err = parseConfigFromCluster(ctx, kubeCl, preparatorProvider, globalOptions, operation)
			return err
		})
	if err != nil {
		return nil, err
	}
	return metaConfig, nil
}

func parseConfigFromCluster(ctx context.Context, kubeCl *client.KubernetesClient, preparatorProvider MetaConfigPreparatorProvider, globalOptions *options.GlobalOptions, operation string) (*MetaConfig, error) {
	metaConfig := &MetaConfig{Operation: operation}

	// panic mitigation
	if globalOptions == nil {
		globalOptions = &options.GlobalOptions{}
	}

	clusterConfig, err := kubeCl.CoreV1().Secrets(global.ConfigsNS).Get(ctx, "d8-cluster-configuration", metav1.GetOptions{})
	if err != nil {
		return nil, err
	}
	clusterConfigData := clusterConfig.Data["cluster-configuration.yaml"]

	var parsedClusterConfig map[string]json.RawMessage
	if err := yaml.Unmarshal(clusterConfigData, &parsedClusterConfig); err != nil {
		return nil, err
	}
	var clusterType string
	if err := json.Unmarshal(parsedClusterConfig["clusterType"], &clusterType); err != nil {
		return nil, err
	}

	var cloudProvider string
	if clusterType == CloudClusterType {
		var cloudSpec struct {
			Provider string `json:"provider"`
		}
		if err := json.Unmarshal(parsedClusterConfig["cloud"], &cloudSpec); err != nil {
			return nil, fmt.Errorf("parse cloud provider from cluster config: %w", err)
		}
		cloudProvider = strings.ToLower(cloudSpec.Provider)
	}
	needProviderCandi := cloudProvider != "" && !providerCandiPresent(cloudProvider, globalOptions)

	// Cloud clusters need registry data even without downloads: provider
	// plugins are pulled lazily and read DeckhouseConfig.RegistryDockerCfg.
	needRegistryData := globalOptions.EnsureCandiAvailable || needProviderCandi || clusterType == CloudClusterType
	if needRegistryData {
		conf, b64dc, err := registrydata.GetRegistryData(ctx, kubeCl)
		if err != nil {
			return nil, err
		}

		if globalOptions.EnsureCandiAvailable {
			if err = prepareCandiDir(ctx, conf, globalOptions); err != nil {
				return nil, err
			}
		}
		if needProviderCandi {
			digest, err := resolveProviderBundleDigest(cloudProvider)
			if err != nil {
				return nil, err
			}
			if err := ensureProviderBundle(ctx, cloudProvider, digest, conf, globalOptions); err != nil {
				return nil, fmt.Errorf("prepare provider bundle: %w", err)
			}
		}

		metaConfig.DeckhouseConfig.RegistryDockerCfg = b64dc
		metaConfig.DeckhouseConfig.ImagesRepo = conf.GetRegistry()
		metaConfig.DeckhouseConfig.RegistryCA = conf.GetCA()
		metaConfig.DeckhouseConfig.RegistryScheme = conf.GetScheme()
	}

	metaConfig.DownloadRootDir = globalOptions.DownloadDir
	metaConfig.DownloadCacheDir = globalOptions.DownloadCacheDir
	metaConfig.VersionFilePath = filepath.Join(globalOptions.DeckhouseDir, "version")
	metaConfig.ShowProgress = globalOptions.ShowProgress

	schemaStore := NewSchemaStore(globalOptions)

	_, err = schemaStore.Validate(&clusterConfigData)
	if err != nil {
		return nil, err
	}

	metaConfig.ClusterConfig = parsedClusterConfig
	metaConfig.ClusterType = clusterType

	_, err = DoByClusterType(ctx, metaConfig, newFromClusterMetaConfigFiller(kubeCl, schemaStore))
	if err != nil {
		return nil, err
	}

	// For cloud clusters, pre-load provider resources from the cluster so that
	// CloudProviderVars is available when the preparator runs. The Cloud filler
	// above has already populated ProviderName (and the mc-flow ModuleConfig or
	// legacy ProviderClusterConfig).
	if clusterType == CloudClusterType {
		cv, err := CloudProviderVarsFromCluster(ctx, kubeCl, metaConfig.ProviderName)
		if err != nil {
			return nil, fmt.Errorf("read cloud provider resources: %w", err)
		}
		metaConfig.CloudProviderVars = cv
	}

	return metaConfig.Prepare(ctx, preparatorProvider)
}

// parseDocument
//
// parse and validate one document of
//
//		InitConfiguration
//		ClusterConfiguration
//		StaticClusterConfiguration
//		ClusterConfiguration
//	    ModuleConfig
//
// if validation schema for ModuleConfig or another resources not found returns ErrSchemaNotFound error
func parseDocument(doc string, metaConfig *MetaConfig, schemaStore *SchemaStore, opts ...ValidateOption) (bool, error) {
	doc = strings.TrimSpace(doc)
	if doc == "" {
		return false, nil
	}

	options := applyOptions(opts...)
	docData := []byte(doc)

	var index SchemaIndex
	err := yaml.Unmarshal(docData, &index)
	if err != nil {
		return false, fmt.Errorf("Config document unmarshal failed: %v\ndata: \n%s\n", err, numerateManifestLines(docData))
	}

	if index.Kind == ModuleConfigKind {
		moduleConfig := ModuleConfig{}
		err = yaml.Unmarshal(docData, &moduleConfig)
		if err != nil {
			return false, fmt.Errorf("Module config document unmarshal failed: %v\ndata: \n%s\n", err, numerateManifestLines(docData))
		}

		log.DebugF("Found ModuleConfig in config file %s\n", moduleConfig.Name)

		if !options.skipSchemaValidation {
			_, err = schemaStore.Validate(&docData, opts...)
			if err != nil {
				if errors.Is(err, ErrSchemaNotFound) {
					log.DebugF("Schema not found for module %s\n", moduleConfig.Name)
					return false, nil
				}
				return false, fmt.Errorf("Module config validation failed: %w\ndata: \n%s\n", err, numerateManifestLines(docData))
			}
		}

		metaConfig.ModuleConfigs = append(metaConfig.ModuleConfigs, &moduleConfig)
		return true, nil
	}

	if !options.skipSchemaValidation {
		_, err = schemaStore.Validate(&docData, opts...)
		if err != nil {
			if errors.Is(err, ErrSchemaNotFound) {
				return false, nil
			}
			return false, fmt.Errorf("Config document validation failed: %v\ndata: \n%s\n", err, numerateManifestLines(docData))
		}
	}

	var data map[string]json.RawMessage
	if err = yaml.Unmarshal(docData, &data); err != nil {
		return false, fmt.Errorf("Config document unmarshal failed: %v\ndata: \n%s\n", err, numerateManifestLines(docData))
	}

	found := false
	switch {
	case index.Kind == "InitConfiguration":
		log.DebugLn("Found InitConfiguration")
		metaConfig.InitClusterConfig = data
		found = true
	case index.Kind == "ClusterConfiguration":
		log.DebugLn("Found ClusterConfiguration")
		metaConfig.ClusterConfig = data
		found = true
	case index.Kind == "StaticClusterConfiguration":
		log.DebugLn("Found StaticClusterConfiguration")
		metaConfig.StaticClusterConfig = data
		found = true
	case strings.HasSuffix(index.Kind, "ClusterConfiguration"):
		log.DebugF("Found %s\n", index.Kind)
		metaConfig.ProviderClusterConfig = data
		found = true
	}

	return found, nil
}

// detectMergedDocuments checks if a document contains multiple YAML documents merged together
// (indicated by multiple top-level "kind:" or "apiVersion:" fields). This happens when the "---" separator is missing.
// When multiple apiVersion fields exist at root level, yaml.Unmarshal will overwrite previous values,
// causing the first document to be lost. This function detects such cases before parsing.
func detectMergedDocuments(doc string) error {
	doc = strings.TrimSpace(doc)
	if doc == "" {
		return nil
	}

	// First, try to unmarshal to see if it fails due to duplicate keys
	// Go's yaml library doesn't error on duplicate keys, but we can detect them by checking
	// if we have multiple root-level apiVersion/kind fields
	var rootLevelKinds []string
	var rootLevelAPIVersions []string
	lines := strings.SplitSeq(doc, "\n")

	for line := range lines {
		trimmed := strings.TrimSpace(line)
		// Skip comments and empty lines
		if trimmed == "" || strings.HasPrefix(trimmed, "#") {
			continue
		}

		// Calculate current indent level (number of leading spaces/tabs)
		leadingWhitespace := len(line) - len(strings.TrimLeft(line, " \t"))

		// Only consider fields at root level (indent 0)
		if leadingWhitespace == 0 {
			if after, ok := strings.CutPrefix(trimmed, "apiVersion:"); ok {
				apiVersionValue := strings.TrimSpace(after)
				if apiVersionValue != "" {
					rootLevelAPIVersions = append(rootLevelAPIVersions, apiVersionValue)
				}
			} else if after, ok := strings.CutPrefix(trimmed, "kind:"); ok {
				kindValue := strings.TrimSpace(after)
				if kindValue != "" {
					rootLevelKinds = append(rootLevelKinds, kindValue)
				}
			}
		}
	}

	// If we have multiple root-level apiVersion or kind fields, documents are likely merged
	// When unmarshaling, duplicate keys will overwrite previous values, causing data loss
	// We check for multiple of both to avoid false positives from nested structures
	if len(rootLevelAPIVersions) > 1 && len(rootLevelKinds) > 1 {
		return fmt.Errorf(
			"invalid config.yml format: missing '---' separator between documents. "+
				"Found multiple root-level 'kind' fields: %v. "+
				"Please ensure each YAML document is separated by '---' on its own line.",
			rootLevelKinds,
		)
	}

	return nil
}

func ParseConfigFromData(
	ctx context.Context,
	configData string, preparatorProvider MetaConfigPreparatorProvider,
	globalOptions *options.GlobalOptions,
	opts ...ValidateOption,
) (*MetaConfig, error) {
	options := applyOptions(opts...)
	schemaStore := NewSchemaStore(globalOptions)

	bigFileTmp := strings.TrimSpace(configData)
	docs := input.YAMLSplitRegexp.Split(bigFileTmp, -1)

	resourcesDocs := make([]string, 0, len(docs))
	errs := &ValidationError{}

	metaConfig := MetaConfig{}
	for _, doc := range docs {
		// Check for merged documents before parsing
		if err := detectMergedDocuments(doc); err != nil {
			wrapped := fmt.Errorf("config validation failed: %w\ndata:\n%s\n", err, numerateManifestLines([]byte(doc)))
			if !options.collectAllErrors {
				return nil, wrapped
			}
			errs.Append(ErrKindInvalidYAML, Error{Messages: []string{wrapped.Error()}})
			continue
		}

		found, err := parseDocument(doc, &metaConfig, schemaStore, opts...)
		if err != nil {
			if !options.collectAllErrors {
				return nil, err
			}
			errs.Append(ErrKindValidationFailed, Error{Messages: []string{err.Error()}})
			continue
		}
		if !found && strings.TrimSpace(doc) != "" {
			var index SchemaIndex
			_ = yaml.Unmarshal([]byte(doc), &index)
			if index.Kind != "" && index.Version != "" {
				log.DebugF("Found resource for resourcesYaml %s/%s\n", index.Kind, index.Version)
			}
			resourcesDocs = append(resourcesDocs, doc)
		}
	}

	if options.collectAllErrors && len(errs.Errors) > 0 {
		// Return the partially-built MetaConfig together with accumulated
		// errors. Callers (e.g. validateCNIBootstrap) can keep analyzing on
		// top of the partial result, so a parse error in one document does
		// not hide a semantic problem in another.
		return &metaConfig, errs
	}

	metaConfig.ResourcesYAML = strings.TrimSpace(strings.Join(resourcesDocs, "\n\n---\n\n"))

	// init configuration can be empty, but we need default from openapi spec
	// Note: InitConfiguration can also be provided via ModuleConfig deckhouse (registry settings)
	if len(metaConfig.InitClusterConfig) == 0 {
		log.DebugF("Init configuration not found, using empty")
		doc := `
apiVersion: deckhouse.io/v1
kind: InitConfiguration
deckhouse: {}
`
		found, err := parseDocument(doc, &metaConfig, schemaStore, opts...)
		if err != nil {
			return nil, err
		}
		if !found {
			return nil, fmt.Errorf("init configuration index not found")
		}
	}

	if options.operation != "" {
		metaConfig.Operation = options.operation
	}
	if options.downloadRootDir != "" {
		metaConfig.DownloadRootDir = options.downloadRootDir
	}

	return metaConfig.Prepare(ctx, preparatorProvider)
}

// ParseConfigFromDataEnsureProvider behaves like ParseConfigFromData but first
// ensures the external provider bundle is downloaded and unpacked (so the
// provider validator binary is available) and points the preparator at the
// download dir. Use it on cold server pods (check/converge/destroy/detach)
// where the bundle is not pre-baked in the install image. Registry access is
// resolved from the registry_config / InitConfiguration / deckhouse ModuleConfig
// documents present in configData.
func ParseConfigFromDataEnsureProvider(
	ctx context.Context,
	configData string,
	preparatorProvider MetaConfigPreparatorProvider,
	globalOptions *options.GlobalOptions,
	opts ...ValidateOption,
) (*MetaConfig, error) {
	docs := input.YAMLSplitRegexp.Split(strings.TrimSpace(configData), -1)
	if err := EnsureProviderBundle(ctx, "", docs, globalOptions); err != nil {
		return nil, err
	}

	opts = append(opts, ValidateOptionDownloadRootDir(resolveDownloadRootDir(globalOptions)))

	return ParseConfigFromData(ctx, configData, preparatorProvider, globalOptions, opts...)
}

// resolveDownloadRootDir returns the root dir for downloaded provider bundles:
// globalOptions.DownloadDir, falling back to a default tmp dir.
func resolveDownloadRootDir(globalOptions *options.GlobalOptions) string {
	if globalOptions.DownloadDir != "" {
		return globalOptions.DownloadDir
	}
	return options.DefaultTmpDir()
}

func FetchDocuments(paths []string) ([]string, error) {
	paths = fs.RevealWildcardPaths(paths)

	content := ""
	for _, path := range paths {
		if strings.Contains(path, "*") {
			continue // skip wildcard paths, we revealed them in the previous step
		}

		log.DebugF("Have config file %s\n", path)
		fileContent, err := os.ReadFile(path)
		if err != nil {
			return nil, fmt.Errorf("loading config file: %w", err)
		}
		content = content + "\n\n---\n\n" + string(fileContent)
	}

	bigFileTmp := strings.TrimSpace(content)
	docs := input.YAMLSplitRegexp.Split(bigFileTmp, -1)

	for _, doc := range docs {
		if err := detectMergedDocuments(doc); err != nil {
			return nil, fmt.Errorf("config validation failed: %w\ndata:\n%s\n", err, numerateManifestLines([]byte(doc)))
		}
	}
	return docs, nil
}

// inTreePreparatorProviders mirrors selectPreparator in infrastructureprovider
// (not imported from here to avoid a cycle).
var inTreePreparatorProviders = map[string]struct{}{
	"yandex": {},
	"vcd":    {},
}

// providerCandiPresent reports whether the provider's schemas — and, for
// external providers, the validator binary — are already on disk, so the
// terraform-manager image download can be skipped.
func providerCandiPresent(provider string, globalOptions *options.GlobalOptions) bool {
	if provider == "" {
		return true
	}
	candiDir := globalOptions.CandiDir
	if candiDir == "" {
		candiDir = options.DefaultCandiDir
	}
	schemaPresent := false
	systemPath := filepath.Join(candiDir, "cloud-providers", provider, "openapi", "cluster_configuration.yaml")
	if _, err := os.Stat(systemPath); err == nil {
		schemaPresent = true
	}
	downloadPath := filepath.Join(providerdata.ProviderDir(globalOptions.DownloadDir, provider), "openapi", "cluster_configuration.yaml")
	if _, err := os.Stat(downloadPath); err == nil {
		schemaPresent = true
	}

	if _, inTree := inTreePreparatorProviders[provider]; inTree {
		return schemaPresent
	}

	// The validator binary ships in the same image as the schemas, so its
	// presence alone marks the bundle as delivered.
	validatorPath := providerdata.ValidatorPath(globalOptions.DownloadDir, provider)
	if _, err := os.Stat(validatorPath); err == nil {
		return true
	}
	return false
}

// buildRegistryConfig derives an OCI registry config from the input documents
// (InitConfiguration and/or ModuleConfig "deckhouse"). Only called when
// candi or provider schemas need to be downloaded.
func buildRegistryConfig(docs []string) (*image.RegistryConfig, error) {
	provider, err := RegistryConfigProvider(docs)
	if err != nil {
		return nil, err
	}
	remoteData, err := provider.RemoteData()
	if err != nil {
		return nil, err
	}
	return image.NewRegistryConfig(
		string(remoteData.Scheme),
		remoteData.ImagesRepo,
		remoteData.Username,
		remoteData.Password,
		remoteData.CA,
	)
}

func RegistryConfigProvider(docs []string) (*registry.ConfigProvider, error) {
	var (
		initConfig        *initconfig.Config
		deckhouseSettings *moduleconfig.DeckhouseSettings
	)

	for _, doc := range docs {
		var obj unstructured.Unstructured
		if err := yaml.Unmarshal([]byte(doc), &obj); err != nil {
			return nil, err
		}

		switch obj.GetKind() {
		case InitConfigurationKind:
			ret, err := registry.ParseYAMLInitConfig([]byte(doc))
			if err != nil {
				return nil, err
			}

			initConfig = ret

		case ModuleConfigKind:
			if obj.GetName() != "deckhouse" {
				continue
			}

			ret, err := registry.ParseYAMLDeckhouseMC([]byte(doc))
			if err != nil {
				return nil, err
			}

			deckhouseSettings = ret
		}
	}
	return registry.NewConfigProvider(initConfig, deckhouseSettings), nil
}

func prepareCandiDir(ctx context.Context, conf *image.RegistryConfig, globalOptions *options.GlobalOptions) error {
	candiImage, err := digests.GetImage("common", "candi")
	if err != nil {
		return err
	}

	imgName := conf.GetRegistry() + "@" + candiImage

	if err = image.DownloadAndUnpackImage(ctx, imgName, globalOptions.DownloadDir, globalOptions.DownloadCacheDir, *conf, globalOptions.ShowProgress); err != nil {
		return err
	}

	err = image.PrepareFiles(filepath.Join(globalOptions.DownloadDir, "deckhouse"))
	if err != nil {
		return err
	}

	return os.MkdirAll(filepath.Join(globalOptions.DownloadDir, "plugins"), 0o755)
}

func fetchCloudProvider(docs []string) (string, error) {
	for _, doc := range docs {
		if err := detectMergedDocuments(doc); err != nil {
			return "", fmt.Errorf("config validation failed: %w\ndata:\n%s\n", err, numerateManifestLines([]byte(doc)))
		}

		var config struct {
			Kind  string `yaml:"kind"`
			Cloud struct {
				Provider string `yaml:"provider"`
			} `yaml:"cloud"`
		}
		if err := yaml.Unmarshal([]byte(doc), &config); err != nil {
			return "", err
		}
		if config.Kind == "ClusterConfiguration" {
			return strings.ToLower(config.Cloud.Provider), nil
		}
	}
	return "", nil
}

var ensureProviderGroup singleflight.Group

// Vars (not funcs) so unit tests can stub digest resolution and the download.
var (
	resolveProviderBundleDigest = func(provider string) (string, error) {
		sectionName := "cloudProvider" + strings.ToUpper(provider[:1]) + provider[1:]
		digest, err := digests.GetImage(sectionName, "terraformManager")
		if err != nil {
			return "", fmt.Errorf("get terraform-manager image digest for provider %s: %w", provider, err)
		}
		return digest, nil
	}
	downloadProviderBundle = image.DownloadAndUnpackImage
)

// EnsureProviderBundle downloads the external provider bundle and loads its
// schemas so the provider becomes validatable in this process. No-op for
// static clusters and already-present candi (in-tree or unpacked). Empty
// provider is extracted from docs; docs also supply registry access (default
// public registry otherwise). Concurrent same-provider calls share one download.
func EnsureProviderBundle(ctx context.Context, provider string, docs []string, globalOptions *options.GlobalOptions) error {
	if globalOptions == nil {
		globalOptions = &options.GlobalOptions{}
	}

	if provider == "" {
		fetched, err := fetchCloudProvider(docs)
		if err != nil {
			return err
		}
		provider = fetched
	}
	provider = strings.ToLower(provider)
	if provider == "" || providerCandiPresent(provider, globalOptions) {
		return nil
	}

	digest, err := resolveProviderBundleDigest(provider)
	if err != nil {
		return err
	}
	if providerBundleReady(provider, digest, globalOptions) {
		return nil
	}

	registryConf, err := buildRegistryConfig(docs)
	if err != nil {
		return fmt.Errorf("registry data to fetch provider bundle for %q: %w", provider, err)
	}
	return ensureProviderBundle(ctx, provider, digest, registryConf, globalOptions)
}

func providerBundleReady(provider, digest string, globalOptions *options.GlobalOptions) bool {
	if !NewSchemaStore(globalOptions).ProviderSchemasLoaded(provider, digest) {
		return false
	}
	_, err := os.Stat(providerdata.ProviderDir(globalOptions.DownloadDir, provider))
	return err == nil
}

func ensureProviderBundle(ctx context.Context, provider, digest string, conf *image.RegistryConfig, globalOptions *options.GlobalOptions) error {
	_, err, _ := ensureProviderGroup.Do(provider+"@"+digest, func() (interface{}, error) {
		if providerBundleReady(provider, digest, globalOptions) {
			return nil, nil
		}
		if err := unpackProviderBundle(ctx, provider, digest, conf, globalOptions); err != nil {
			return nil, err
		}
		// Load from the real digest dir: filepath.Walk does not follow the
		// <provider> symlink root.
		digestDir := providerdata.ProviderDigestDir(globalOptions.DownloadDir, provider, digest)
		return nil, NewSchemaStore(globalOptions).LoadProviderDir(provider, digest, digestDir)
	})
	return err
}

func unpackProviderBundle(ctx context.Context, provider, digest string, conf *image.RegistryConfig, globalOptions *options.GlobalOptions) error {
	digestDir := providerdata.ProviderDigestDir(globalOptions.DownloadDir, provider, digest)
	if _, err := os.Stat(digestDir); err != nil {
		imgName := conf.GetRegistry() + "@" + digest
		log.DebugF("Downloading provider bundle for %s\n", provider)
		if err := downloadProviderBundle(ctx, imgName, digestDir, globalOptions.DownloadCacheDir, *conf, globalOptions.ShowProgress); err != nil {
			return fmt.Errorf("download provider bundle %s: %w", imgName, err)
		}
	}
	return switchProviderSymlink(providerdata.ProviderDir(globalOptions.DownloadDir, provider), digestDir)
}

// switchProviderSymlink atomically points linkPath at target. A pre-symlink
// layout may have left a real directory at linkPath — it is replaced.
func switchProviderSymlink(linkPath, target string) error {
	if info, err := os.Lstat(linkPath); err == nil && info.Mode()&os.ModeSymlink == 0 {
		if err := os.RemoveAll(linkPath); err != nil {
			return fmt.Errorf("remove legacy provider dir %s: %w", linkPath, err)
		}
	}
	tmp := linkPath + ".tmp"
	_ = os.Remove(tmp)
	if err := os.Symlink(target, tmp); err != nil {
		return fmt.Errorf("create provider symlink: %w", err)
	}
	if err := os.Rename(tmp, linkPath); err != nil {
		return fmt.Errorf("activate provider symlink: %w", err)
	}
	return nil
}

// prepare CandiDir if not exists
