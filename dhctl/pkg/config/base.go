// GetEdition
// returns editions map in lower case
func EEEditions() map[string]struct{} {
	editions := strings.Split(CommaSeparatedEEEditions, ",")

	res := make(map[string]struct{})
	for _, e := range editions {
		e = strings.TrimSpace(e)
		if e != "" {
			res[strings.ToLower(e)] = struct{}{}
		}
	}

	return res
}

func IsEEEdition(ed string) bool {
	ed = strings.ToLower(ed)
	editions := EEEditions()
	_, ok := editions[ed]
	return ok
}

func IsCSEdition(ed string) bool {
	return strings.ToLower(ed) == "cse"
}
