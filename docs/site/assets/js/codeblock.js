function resolveDocLang() {
  let lang = document.documentElement.lang;

  if (lang.length === 0) {
    if (window.location.href.includes("deckhouse.ru") || window.location.href.includes("deckhouse-ru") || window.location.href.includes("ru.localhost")) {
      lang = "ru";
    } else {
      lang = "en";
    }
  }

  if (!($.cookie("lang") === lang)) {
    $.cookie('lang', lang, {path: '/', expires: 365});
  }

  return lang;
}

function createLineNumber(index) {
  const lineNumber = document.createElement('span');
  lineNumber.setAttribute('data-copy', 'ignore');
  lineNumber.classList.add('line-number');
  lineNumber.textContent = String(index);
  return lineNumber;
}

function ensureLineNumbers(code) {
  const directLineNodes = Array.from(code.querySelectorAll(':scope > .line'));

  if (directLineNodes.length > 0) {
    directLineNodes.forEach((lineNode, index) => {
      if (!lineNode.querySelector(':scope > .line-number')) {
        lineNode.prepend(createLineNumber(index + 1));
      }
    });
    return;
  }

  // Jekyll/Rouge can output highlighted tokens without top-level `.line` wrappers.
  // Preserve that token markup here; Hugo/Chroma goes through the `.line` branch above.
  const hasSyntaxMarkup = !!code.querySelector('span');
  if (hasSyntaxMarkup) {
    // Preserve existing syntax-highlighting markup generated by Rouge/Chroma.
    const codeHtml = code.innerHTML;
    const lines = codeHtml.split('\n');
    while (lines.length > 0 && lines[lines.length - 1].trim() === '') {
      lines.pop();
    }
    let newHTML = '';
    let lineIndex = 1;

    lines.forEach((line, index, arr) => {
      const trimLine = line.trim();
      if (/^(\s*<\/span>\s*){1,3}$/.test(trimLine)) {
        return;
      }

      if (trimLine.length === 0) {
        newHTML += '\n';
        return;
      }

      newHTML += `<span data-copy="ignore" class="line-number">${lineIndex}</span>${line}`;
      if (index < arr.length - 1) {
        newHTML += '\n';
      }
      lineIndex++;
    });

    code.innerHTML = newHTML;
    return;
  }

  const lines = (code.textContent || '').split('\n');
  if (lines.length > 1 && lines[lines.length - 1] === '') {
    lines.pop();
  }

  const fragment = document.createDocumentFragment();
  lines.forEach((lineText, index) => {
    const lineNode = document.createElement('span');
    lineNode.classList.add('line');
    lineNode.appendChild(createLineNumber(index + 1));

    const clNode = document.createElement('span');
    clNode.classList.add('cl');
    clNode.textContent = lineText;
    lineNode.appendChild(clNode);

    fragment.appendChild(lineNode);
    if (index < lines.length - 1) {
      fragment.appendChild(document.createTextNode('\n'));
    }
  });

  code.innerHTML = '';
  code.appendChild(fragment);
}

function extractCopyText(code) {
  let textToCopy = '';

  function walk(node) {
    if (node.nodeType === Node.TEXT_NODE) {
      textToCopy += node.textContent;
      return;
    }

    if (node.nodeType === Node.ELEMENT_NODE && node.getAttribute('data-copy') === 'ignore') {
      return;
    }

    for (let i = 0; i < node.childNodes.length; i++) {
      walk(node.childNodes[i]);
    }
  }

  for (let i = 0; i < code.childNodes.length; i++) {
    walk(code.childNodes[i]);
  }

  return textToCopy;
}

document.addEventListener('DOMContentLoaded', function() {
  const preElements = document.querySelectorAll('pre');
  if (!preElements.length) {
    return;
  }

  const lang = resolveDocLang();
  const textTooltip = {
    en: {
      copy: 'Copy',
      copied: 'Copied!',
      error: 'Error!',
      wrap: 'Wrap',
      unwrap: 'Unwrap',
    },
    ru: {
      copy: 'Копировать',
      copied: 'Скопировано!',
      error: 'Ошибка!',
      wrap: 'Переносить строки',
      unwrap: 'Не переносить строки',
    }
  };
  const texts = textTooltip[lang];

  preElements.forEach((pre) => {
    if (pre.dataset.codeblockInitialized === 'true') {
      return;
    }

    if (pre.querySelector('code.language-tree')) {
      pre.dataset.codeblockInitialized = 'true';
      return;
    }

    pre.classList.add('code__transfer');
    const code = pre.querySelector('code');
    if (!code) {
      pre.dataset.codeblockInitialized = 'true';
      return;
    }

    ensureLineNumbers(code);

    const isNowrapDefault = pre.closest('.nowrap-default') !== null;
    if (!isNowrapDefault) {
      code.classList.add('wrap');
    }

    let button = pre.querySelector(':scope > .wrap__button');
    let wrapIcon;
    let unwrapIcon;

    if (!button) {
      button = document.createElement('button');
      button.classList.add('wrap__button');

      wrapIcon = document.createElement('img');
      wrapIcon.src = '/images/wrap-button.svg';
      wrapIcon.classList.add('wrap__icon');
      wrapIcon.style.display = 'none';

      unwrapIcon = document.createElement('img');
      unwrapIcon.src = '/images/unwrap-button.svg';
      unwrapIcon.classList.add('wrap__icon');

      button.appendChild(wrapIcon);
      button.appendChild(unwrapIcon);
      pre.appendChild(button);
    } else {
      wrapIcon = button.querySelector('.wrap__icon:first-child');
      unwrapIcon = button.querySelector('.wrap__icon:last-child');
    }

    let isWrapped = false;
    button.classList.remove('show');

    if (!button.dataset.wrapInitialized) {
      button.addEventListener('click', function() {
        isWrapped = !isWrapped;
        code.classList.toggle('wrap');

        if (isWrapped) {
          wrapIcon.style.display = 'inline';
          unwrapIcon.style.display = 'none';
        } else {
          wrapIcon.style.display = 'none';
          unwrapIcon.style.display = 'inline';
        }
      });

      tippy(wrapIcon, {
        placement: 'left',
        arrow: false,
        animation: 'scale',
        theme: 'light',
        content: texts.wrap,
        hideOnClick: false,
        delay: [300, 50],
        offset: [0, 10],
        duration: [300],
      });

      tippy(unwrapIcon, {
        placement: 'left',
        arrow: false,
        animation: 'scale',
        theme: 'light',
        content: texts.unwrap,
        hideOnClick: false,
        delay: [300, 50],
        offset: [0, 10],
        duration: [300],
      });

      button.dataset.wrapInitialized = 'true';
    }

    pre.addEventListener('mouseenter', () => {
      button.classList.add('show');

      const existingCopyBtn = pre.querySelector('.icon--copy');
      if (existingCopyBtn) {
        existingCopyBtn.classList.add('show');
        return;
      }

      const copyBtn = document.createElement('div');
      copyBtn.classList.add('icon--copy');
      pre.prepend(copyBtn);

      const copyBtnTippy = tippy(copyBtn, {
        placement: 'left',
        arrow: false,
        animation: 'scale',
        theme: 'light',
        content: texts.copy,
        hideOnClick: false,
        delay: [300, 50],
        offset: [0, 10],
        duration: [300],
      });

      copyBtn.addEventListener('click', () => {
        if (!code) {
          return;
        }

        const textToCopy = extractCopyText(code);

        navigator.clipboard.writeText(textToCopy).then(() => {
          copyBtnTippy.setContent(texts.copied);
          setTimeout(() => {
            copyBtnTippy.hide();
          }, 1000);
        }, () => {
          copyBtnTippy.setContent(texts.error);
          setTimeout(() => {
            copyBtnTippy.hide();
          }, 1000);
        });
      });

      setTimeout(() => {
        if (copyBtn.isConnected) {
          copyBtn.classList.add('show');
        }
      }, 0);
    });

    pre.addEventListener('mouseleave', () => {
      button.classList.remove('show');

      const copyBtn = pre.querySelector('.icon--copy');
      if (!copyBtn) {
        return;
      }

      copyBtn.classList.remove('show');
      setTimeout(() => {
        const currentCopyBtn = pre.querySelector('.icon--copy');
        if (currentCopyBtn && !currentCopyBtn.classList.contains('show')) {
          currentCopyBtn.remove();
        }
      }, 300);
    });

    pre.dataset.codeblockInitialized = 'true';
  });
});
