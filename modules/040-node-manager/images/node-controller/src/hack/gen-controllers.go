// This program generates cmd/controllers.go by scanning
// internal/controller/ for subdirectories containing controller.go.
// Usage: go run hack/gen-controllers.go

package main

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

const (
	modulePath    = "github.com/deckhouse/node-controller"
	controllerDir = "../internal/controller" // relative to cmd/
	importPath    = "internal/controller"
	outputFile    = "controllers.go" // generated in cmd/
)

func main() {
	entries, err := os.ReadDir(controllerDir)
	if err != nil {
		fmt.Fprintf(os.Stderr, "error reading %s: %v\n", controllerDir, err)
		os.Exit(1)
	}

	var packages []string
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		controllerFile := filepath.Join(controllerDir, entry.Name(), "controller.go")
		if _, err := os.Stat(controllerFile); err == nil {
			packages = append(packages, entry.Name())
		}
	}
	sort.Strings(packages)

	var imports []string
	for _, pkg := range packages {
		imports = append(imports, fmt.Sprintf("\t_ \"%s/%s/%s\"", modulePath, importPath, pkg))
	}

	content := fmt.Sprintf(`// Code generated by hack/gen-controllers.go; DO NOT EDIT.

package main

import (
%s
)
`, strings.Join(imports, "\n"))

	if err := os.WriteFile(outputFile, []byte(content), 0644); err != nil {
		fmt.Fprintf(os.Stderr, "error writing %s: %v\n", outputFile, err)
		os.Exit(1)
	}

	fmt.Printf("Generated %s with %d controller(s): %s\n", outputFile, len(packages), strings.Join(packages, ", "))
}
