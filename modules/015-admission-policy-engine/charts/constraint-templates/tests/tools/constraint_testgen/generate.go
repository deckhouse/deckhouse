const generatedHeader = "# Code generated by constraint_testgen; DO NOT EDIT.\n\n"

type suiteOut struct {
	Kind       string `yaml:"kind"`
	APIVersion string `yaml:"apiVersion"`
	Metadata   struct {
		Name string `yaml:"name"`
	} `yaml:"metadata"`
	Tests []testBlockOut `yaml:"tests"`
}

type testBlockOut struct {
	Name       string    `yaml:"name"`
	Template   string    `yaml:"template"`
	Constraint string    `yaml:"constraint"`
	Cases      []caseOut `yaml:"cases"`
}

type externalDataOut struct {
	Providers []interface{} `yaml:"providers,omitempty"`
}

type caseOut struct {
	Name         string                   `yaml:"name"`
	Inventory    []string                 `yaml:"inventory,omitempty"`
	Object       string                   `yaml:"object"`
	ExternalData *externalDataOut         `yaml:"externalData,omitempty"`
	Assertions   []map[string]interface{} `yaml:"assertions"`
}

// generateFromInputFile accepts only ConstraintTestMatrix and dispatches generation.
func generateFromInputFile(path, testsRoot string) error {
	raw, err := os.ReadFile(path)
	if err != nil {
		return fmt.Errorf("read input: %w", err)
	}
	var head struct {
		Kind string `yaml:"kind"`
	}
	if err := yaml.Unmarshal(raw, &head); err != nil {
		return fmt.Errorf("parse kind: %w", err)
	}
	if head.Kind != "ConstraintTestMatrix" {
		return fmt.Errorf("unsupported kind %q (expected ConstraintTestMatrix)", head.Kind)
	}
	return generateFromMatrix(path, testsRoot)
}

func outputPathForWrite(path string, samplesDir, outDir string) string {
	p := filepath.ToSlash(path)
	if strings.HasPrefix(p, "test_samples/") {
		return filepath.Join(samplesDir, filepath.FromSlash(strings.TrimPrefix(p, "test_samples/")))
	}
	return filepath.Join(outDir, filepath.FromSlash(path))
}
