package openapigen

import (
	"fmt"

	"dario.cat/mergo"
	"github.com/getkin/kin-openapi/openapi3"
	apiextensionsv1 "k8s.io/apiextensions-apiserver/pkg/apis/apiextensions/v1"
	ctmarkers "sigs.k8s.io/controller-tools/pkg/markers"
	"sigs.k8s.io/yaml"

	"openapigen/internal/deckhouse"
	"openapigen/internal/kube"
	"openapigen/markers"
)

const (
	defaultHeader = "# This file is auto-generated by openapigen. Do not edit manually.\n"
)

// SchemaConfig holds the configuration for schema generation.
// It is a pure value type with no mutable state.
type SchemaConfig struct {
	// EnableKubebuilderMarkers enables +kubebuilder:validation:* marker collection
	// via controller-tools crd.Parser.
	EnableKubebuilderMarkers bool

	// EnableDeckhouseMarkers enables deckhouse x-* marker collection via
	// deckhouse.BuildSchema.
	EnableDeckhouseMarkers bool

	// DeckhouseRegistry is the deckhouse marker registry to use.
	// If nil and EnableDeckhouseMarkers is true, BuildDeckhouseOpenAPIMarkerRegistry()
	// is used automatically.
	DeckhouseRegistry *ctmarkers.Registry
}

// SchemaGenerator generates OpenAPI schemas from Go types.
// It is stateless and safe for concurrent use after construction.
type SchemaGenerator struct {
	cfg SchemaConfig
}

// NewSchemaGenerator creates a new SchemaGenerator with the given config.
// Returns an error if both EnableKubebuilderMarkers and EnableDeckhouseMarkers are false.
func NewSchemaGenerator(cfg SchemaConfig) (*SchemaGenerator, error) {
	if !cfg.EnableKubebuilderMarkers && !cfg.EnableDeckhouseMarkers {
		return nil, fmt.Errorf("SchemaConfig: at least one of EnableKubebuilderMarkers or EnableDeckhouseMarkers must be true")
	}
	return &SchemaGenerator{cfg: cfg}, nil
}

// Generate produces a typed *openapi3.Schema for root.
// root must be a struct or a pointer to a struct.
// All intermediate values are local — no state is written to SchemaGenerator.
func (g *SchemaGenerator) Generate(root any) (*openapi3.Schema, error) {
	// resolve registry
	reg := g.cfg.DeckhouseRegistry
	if g.cfg.EnableDeckhouseMarkers && reg == nil {
		var err error
		reg, err = markers.BuildDeckhouseOpenAPIMarkerRegistry()
		if err != nil {
			return nil, fmt.Errorf("build deckhouse marker registry: %w", err)
		}
	}

	var kubebuilderSchema *openapi3.Schema
	if g.cfg.EnableKubebuilderMarkers {
		props, err := kube.GetJSONSchemaPropsFromDefaultMarkers(root)
		if err != nil {
			return nil, fmt.Errorf("kubebuilder schema: %w", err)
		}
		kubebuilderSchema, err = jsonSchemaPropsToOpenAPI(props)
		if err != nil {
			return nil, fmt.Errorf("convert kubebuilder schema: %w", err)
		}
	}

	var deckhouseSchema *openapi3.Schema
	if g.cfg.EnableDeckhouseMarkers {
		var err error
		deckhouseSchema, err = deckhouse.BuildSchema(root, reg)
		if err != nil {
			return nil, fmt.Errorf("deckhouse schema: %w", err)
		}
	}

	// merge base: deep-copy kubebuilder schema or start empty
	var base *openapi3.Schema
	if kubebuilderSchema != nil {
		var err error
		base, err = cloneOpenAPISchema(kubebuilderSchema)
		if err != nil {
			return nil, fmt.Errorf("clone kubebuilder schema: %w", err)
		}
	} else {
		base = &openapi3.Schema{}
	}

	if deckhouseSchema != nil {
		if err := mergo.Merge(base, deckhouseSchema, mergo.WithOverride); err != nil {
			return nil, fmt.Errorf("merge schemas: %w", err)
		}
	}

	return base, nil
}

// GenerateYAML serializes the result of Generate to YAML, prepending the default header.
func (g *SchemaGenerator) GenerateYAML(root any) ([]byte, error) {
	schema, err := g.Generate(root)
	if err != nil {
		return nil, err
	}
	out, err := yaml.Marshal(schema)
	if err != nil {
		return nil, fmt.Errorf("marshal schema: %w", err)
	}
	return append([]byte(defaultHeader), out...), nil
}

// jsonSchemaPropsToOpenAPI converts JSONSchemaProps to openapi3.Schema via YAML round-trip.
func jsonSchemaPropsToOpenAPI(src *apiextensionsv1.JSONSchemaProps) (*openapi3.Schema, error) {
	data, err := yaml.Marshal(src)
	if err != nil {
		return nil, fmt.Errorf("marshal JSONSchemaProps: %w", err)
	}
	out := &openapi3.Schema{}
	if err := yaml.Unmarshal(data, out); err != nil {
		return nil, fmt.Errorf("unmarshal to openapi3.Schema: %w", err)
	}
	return out, nil
}

// cloneOpenAPISchema deep-clones an openapi3.Schema via YAML round-trip.
func cloneOpenAPISchema(src *openapi3.Schema) (*openapi3.Schema, error) {
	data, err := yaml.Marshal(src)
	if err != nil {
		return nil, fmt.Errorf("marshal openapi3.Schema: %w", err)
	}
	out := &openapi3.Schema{}
	if err := yaml.Unmarshal(data, out); err != nil {
		return nil, fmt.Errorf("unmarshal openapi3.Schema: %w", err)
	}
	return out, nil
}

// GenerateDeckhouseOpenAPISchema generates the combined kubebuilder + deckhouse
// OpenAPI schema YAML for root.
func GenerateDeckhouseOpenAPISchema(root any) ([]byte, error) {
	reg, err := markers.BuildDeckhouseOpenAPIMarkerRegistry()
	if err != nil {
		return nil, err
	}
	gen, err := NewSchemaGenerator(SchemaConfig{
		EnableKubebuilderMarkers: true,
		EnableDeckhouseMarkers:   true,
		DeckhouseRegistry:        reg,
	})
	if err != nil {
		return nil, err
	}
	return gen.GenerateYAML(root)
}

// GenerateDeckhouseDescriptionRu generates a sparse ru-description overlay
// YAML for root (only deckhouse ru:description markers are collected).
func GenerateDeckhouseDescriptionRu(root any) ([]byte, error) {
	reg, err := markers.BuildDeckhouseDescriptionRuOpenAPIMarkerRegistry()
	if err != nil {
		return nil, err
	}
	gen, err := NewSchemaGenerator(SchemaConfig{
		EnableDeckhouseMarkers: true,
		DeckhouseRegistry:      reg,
	})
	if err != nil {
		return nil, err
	}
	return gen.GenerateYAML(root)
}
